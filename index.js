// Control flow:

// Loops : repeat an action for no. of times

// Break and Continue

let i = 0;
while (i <= 10) {
    if (i === 5) break;
    if (i % 2 === 0) {
        i++;
        continue;
    }

    console.log(i);
    i++;
}

// We use 'break' keyword to jump out of the loop and use 'continue' keyword we jump to the next iteration










